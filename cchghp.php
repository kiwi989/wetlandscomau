<p>
<b>Project Name:</b> Campbell Creek - Harrington Grove, Harrington Park<br />
<b>Client:</b> Hassell (Planning & Design) & Harpak Developments<br />
<b>Value of Works Portion:</b> $1M - $1.3M range<br />
<b>Year:</b> 2009 to present<br />
<b>Land Area:</b> 72,430m2<br />
</p>
<p>
<b>Project Description</b><br /><br />

The Harrington Grove development is located in South-Western Sydney. It is on a 280 hectare parcel of land. 60% of the site is conserved or restored natural environment. Australian Wetlands was employed by the Harrington Park Development Team as the preferred revegetation and regeneration contractor. This project was one of numerous projects Australian Wetlands have carried out within the Harrington Park estate.
</p>
<p>
<b>Services Provided</b><br /><br />

Australian Wetlands was engaged to revegetate and regenerate the site in accordance with the VMP and DWE approval. Our ecological services to the project included:<br />
<ul>
<li>Local provenance seed collection and propagation of 230,988 plants</li>
<li>2.2km of temporary parawebb fencing</li>
<li>Soil testing and soil amelioration</li>
<li>Erosion control - jute matting and log habitat creation in channel</li>
<li>Large-scale revegetation landscaping including 33,156 tree guards</li>
<li>Primary and secondary bush regeneration</li>
<li>Temporary irrigation</li>
<li>2-year plant establishment maintenance period</li> 
</ul>
As the main ecological contractor for Harrington Park, Australian Wetlands continue to guarantee an aesthetically pleasing, successfully implemented project that satisfies our clients, DWE and Camden Council requirements.   
</p>

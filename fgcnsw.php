<p>
<b>Project Name:</b> Fishers Ghost Creek<br />
<b>Client:</b> Campbelltown City Council<br /> 
<b>Value of Works Portion:</b>$30-$60K range<br />
<b>Year:</b> 2007<br />
</p>
<p>
<b>Project Description</b><br /><br />

Australian Wetlands was engaged by Campbelltown City Council to construct a 105 metre long creek waterfeature at Campbelltown Art Gallery.
</p>
<p>
<b>Services Provided</b><br /><br />

Australian Wetlands was engaged to undertake the construction of the waterfeature. Our scope included:<br />
<ul>
<li>On-site design advise</li> 
<li>Minor trimming</li>
<li>Waterproof membrane</li>
<li>Coloured fibrecrete concrete channel with sandstone rocks slung into wet concrete</li>
<li>Erosion control</li>
<li>Interplanting with grasses & sedges in sandstone rock mulch</li>
<li>Concrete edging</li>
<li>Turfing</li>
<li>13-week maintenance contract</li>
</ul>
The project demonstrates Australian Wetlands' technical abilities & client orientated focus.
</p>

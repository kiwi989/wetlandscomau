<p>
<b>Project Name:</b>Tugun Bypass Upgrade<br />
<b>Client:</b> PacificLink Alliance (SMEC, Abigroup, Main Roads)<br /> 
<b>Value of Works Portion:</b>$1.2-$1.5 million<br />
<b>Year:</b> 2006 to 2009<br />
</p>

<b>Project Description</b><br />
<p>The Tugun Bypass is a 7.5km roadway which laws built to link south-east Queensland and Northern New South Wales by bypassing the suburb of Tugun. Valued at $400mil, the project comprised the design and construction of 4-lane highway and associated infrastructure including a 400m tunnel under the Gold Coast Airport runway. 
</p>
<p>
Delivery of the project was managed by the PacificLink Alliance, comprising Abigroup, SMEC and The Department of Main Roads. The Australian Wetlands project team was successful in obtaining a key role on this project.</p>

<b>Services Provided</b>
<br />
<p>Australian Wetlands provided a range of services to the project including: 
<ul>
<li>Whole-of-project landscaping, revegetation and maintenance</li>
<li>Development - in collaboration with Waterway Solutions - of site-specific water quality and stormwater management strategy</li>
<li>Wetlands translocation and construction of frog ponds</li>
<li>Input and implementation of site Environmental Management Plan</li>
<li>Water quality monitoring</li>
</ul><br />
This project provided a showcase of Australian Wetlandsâ€™ ability to provide client-focused construction solutions.</p>

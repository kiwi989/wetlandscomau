<p>
<b>Project Name:</b> Precinct 6 Raingarden - The Ponds<br />
<b>Client:</b> Burton Contractors<br /> 
<b>Value of Works Portion:</b>$100K-$200K range<br />
<b>Year:</b> 2009<br />
<b>Land Area:</b> 1,580m2<br />
</p>
<p>
<b>Project Description</b><br /><br />

The Precinct 6 Raingarden is located at the Ponds in the North-West of Sydney. The raingarden was built by Australian Wetlands for the Landcom & Australand development to retain and treat water in accordance with MUSIC modelling and best WSUD industry practices.
</p>
<p>
<b>Services Provided</b><br /><br />

Australian Wetlands was engaged to undertake the bio-retention civil works. Our scope included:<br />
<ul>
<li>HDPE liner</li> 
<li>Bio-retention drainage pipework connected to stormwater pits</li>
<li>Bio-retention media</li>
<li>Hydraulic conductivity testing</li>
<li>Jute matting</li>
</ul>
The completed project illustrates Australian Wetlands’ ability to successfully implement our technical expertise in WSUD construction.
</p>

<b>Our key personnel are as follows:</b><br /><br /> 

<p>
<b>Peter Adcock - Company Director & Community Restoration Program</b><br /><br />

Peter has a Bachelor of Science with Honours (Botany 1st Class) from the University of Adelaide. He is recognised as one of the pioneers in the wetland, stormwater and environment industries and has written botany/wetland related publications. He has acted as a conference speaker and has been involved in workshop presentations. Peter has been employed as an expert witness on high profile court cases. Peter commercialised Australian Wetlands in 1993 from a $2M wetland research project at the University of Western Sydney Hawkesbury.  
</p>
<p>
<b>Steve Moore - CEO</b><br /><br />
Steve was appointed as General Manager Construction and Landscape in October 2004. Steve brings 25 years of experience in Landscape Construction and Maintenance to Australian Wetlands. Steve now fulfills the role where he oversees the national expansion and development of the company. Over the years, Steve has worked as Foreman, Project Manager, Estimator, OHSE Manager, Business Development Manager and General Manager for some of Sydney’s biggest landscape construction companies. Steve’s experience covers a diverse range of projects from major structural/civil projects to large revegetation/rehabilitation and maintenance projects. Steve has also recently completed a significant estuarine design project in the Middle East.  Steve has an Associate Diploma of Applied Science (Landscape) and is a member of the Australian Institute of Landscape Designers and Managers. He is based in our Sunshine Coast office. 
</p>
<p>
<b>Damian McCann - Australian Wetlands Consulting Pty Ltd</b><br /><br />
Damian holds a Bachelor of Applied Science, Social Ecology with an Honours Degree in Science (Ecology), as well as qualifications in Bushland Regeneration and Horticulture. Damian has worked in the fields of environmental consulting, design and ecological restoration since 1996. Areas of expertise include ecological restoration, project management, stormwater and wastewater management. On behalf of Australian Wetlands, Damian co-authored ‘Best Practice Guidelines for the Construction of WSUD Devices for the QLD Healthy Waterways’. Damian is an industry leading speaker and technical expert.
</p>
<p>
<b>Derick Moulds - National Business Development Manager</b><br /><br /> 
Derick has a Bachelor of Landscape Management and Conservation from the University of Western Sydney and a Certificate IV in Small Business Management (including a NSW TAFE State Medal - 1st place in the State). He has ten years experience in the landscape construction & environmental industry (including six years at Australian Wetlands). During this time, Derick has been employed in sales, estimating, operations, field supervision, maintenance management and marketing.
</p>
<p>
<b>Glen McMaster - General Manager (Sydney Bioregion)</b><br /><br />
Glen has completed a Bachelor of Environmental Science from the University of Western Sydney Hawkesbury. He has over six years experience in the landscaping and environmental industry with Australian Wetlands. Glen’s previous roles have included Site Management, Project Management, and Landscape Operations. Glen now undertakes the role of General Manager and is responsible for the Operations, Commercial and Estimating sections. 
</p>

<p> 
<b>Project Description</b><br /><br />
Australian Wetlands was engaged to undertake a streetscaping project for a commercial development in Zetland. The work was carried out in the inner city in a highly urbanised environment.
</p>
<p>
<b>Services Provided</b><br /><br />
Australian Wetlands scope of work included:
<ul>
	<li>Soil testing</li>
	<li>26x street tree pits including 100L tree planting, soil, irrigation system, irrigation inspection grates, porphyry sets with Terrabond jointing and staking & tying</li>
	<li>Root control barrier including trenching</li>
	<li>Hydroseeding</li> 
	<li>Mass planting including topsoil, mulching and plant installations</li> 
	<li>Turfing</li>
	<li>Establishment watering and 12-months maintenance</li>
</ul>

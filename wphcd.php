<p>
<b>Project Name:</b> Wetland & Plateau - Holsworthy<br />
<b>Client:</b> Thomas & Coffey<br /> 
<b>Value of Works Portion:</b>$750K - $900K range<br />
<b>Year:</b> 2009<br />
<b>Land Area:</b> 93,350m2<br />
</p>
<p>
<b>Project Description</b><br /><br />

The Holsworthy Civil Development located in South-Western Sydney was built for the Australian Government. Australian Wetlands role was to construct two wetland basins which retain and treat water as well as being a source of water for fire emergencies. Australian Wetlands also applied hydro-mulch to a large plateau on site to achieve native grass revegetation. 
</p>
<p>
<b>Services Provided</b><br /><br />

Australian Wetlands was engaged to provide landscaping, restoration and civil services to the project including:<br />
<ul>
<li>15,750m2 of wetland construction including wetland HDPE lining, soil media to 300mm depth and jute matting</li>
<li>Basin softworks, including installation of 55,000 plants and 4,300m2 of turf</li>
<li>Plateau stabilisation (73,300m2 of native grass hydro-mulching and 12,385m2 of jute mesh)</li>
<li>Establishment maintenance and monitoring for 12-months</li> 
</ul>
The completed project showcases Australian Wetlands’ ability to integrate technical, ecological and construction expertise into large scale landscape basin and restoration construction projects.
</p>

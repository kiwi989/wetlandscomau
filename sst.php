<p>
<b>Project Name:</b> Southport Seagrass Translocation<br />
<b>Client:</b> Abigroup & Gold Coast Council<br /> 
<b>Value of Works Portion:</b> $100K-$200K range<br />
<b>Year:</b> 2009<br />
</p>
<p> 
<b>Project Description</b><br /><br />
The Southport Broadwater Parklands redevelopment is a public domain project jointly funded by Gold Coast City Council (GCCC) and the QLD state government, with Abigroup as the chosen construction contractor.<br />
<br />
The development approval for the site required the transplant of Zostera halophila, a native seagrass species as well as various other marine species such as mangroves and samphires. Australian Wetlands were jointly engaged by Abigroup and GCCC to undertake the required works.
</p>
<p>
<b>Services Provided</b><br /><br />
<ul>
	<li>Translocation of approx 800m2 of aquatic Zostera meadows</li>
	<li>Translocation of mangroves, salt couch rhizomes and assorted samphires</li>
	<li>GIS mapping</li>
	<li>Monitoring and maintenance of transplants</li> 
</ul>
This project showcases Australian Wetlands ability to deliver a practical and cost-effective on-ground solution to the environmental challenges facing large and medium-scale infrastructure projects.
</p>

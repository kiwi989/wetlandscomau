<p>
<b>Project Name:</b> Tributary 1 - Fairwater Drive, Harrington Park<br /> 
<b>Client:</b> Hassell Planning & Design, Harpak Developments<br />
<b>Value of Works Portion:</b> $500K-$750K range<br />
<b>Year:</b> 2006-2009<br />
<b>Land Area:</b> 50,000m2<br />
</p>
<p>
<b>Project Description</b><br /><br />

Harrington Park is a nationally-recognised multi-award winning estate located in the South-West of Sydney. The development’s success rests in the dream of Sir Warwick Fairfax and the hard work of Lady Mary Fairfax and the entire Harrington Park Development Team even after the death of Sir Warwick. The Tributary 1 project is one of numerous projects Australian Wetlands have carried out within the estate. Australian Wetlands is traditionally Harpak Developments preferred revegetation and regeneration contractor.
</p>
<p>
<b>Services Provided</b><br /><br />

Australian Wetlands was engaged to revegetate and regenerate a large tributary and surrounds in accordance with our VMP and DNR 3a permit. Our ecological services to the project included:<br />
<ul>
<li>Local provenance seed collection and propagation of 205,640 plants</li>
<li>Soil testing and soil amelioration</li>
<li>Erosion control - jute matting and silt fencing</li>
<li>Water control (dewatering)</li>
<li>Large-scale revegetation landscaping</li>
<li>Primary and secondary bush regeneration</li>
<li>Temporary irrigation</li>
<li>2-year plant establishment maintenance period of revegetation and regeneration</li> 
</ul>
At the completion of maintenance, Australian Wetlands handed over the project to Camden Council. It was hailed as a complete success by our client, Council and DNR.    
</p>

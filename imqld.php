<p>
<b>Project Name:</b>  Ingham's Murarrie<br />
<b>Client:</b> Badge Constructions<br />
<b>Value of Works Portion:</b> $150K-$250K range<br />
<b>Year:</b> 2009<br />
<b>Land Area:</b> 11,022m2<br />
</p>
<p>
<b>Project Description</b><br /><br />
Ingham's poultry processing plant located in Brisbane's south-east has two constructed stormwater treatment wetlands on site. A development application to upgrade to the plant was submitted to Brisbane City Council. A condition of the approval was a site-specific rehabilitation plan and it's implementation. 
</p>
<p>
<b>Services Provided</b><br /><br />
Australian Wetlands was engaged to implement the plan. Wetland refurbishment and revegetation services to the project included:<br />
<ul>
	<li>Dewatering</li>
	<li>Excavation of organic matter & removal of rubbish</li>
	<li>Weed control and removal</li>
	<li>482m3 of soil works</li>
	<li>3,660m2 of jute matting & 28,770 plants</li>
	<li>Establishment maintenance and monitoring for 12-months</li>
</ul>
Australian Wetlands' practical experience and expertise was called upon to refurbish the wetlands.
</p>

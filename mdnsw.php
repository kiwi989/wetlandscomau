<p>
<b>Project Name:</b> Madza Dealership<br />
<b>Client:</b> Wishton, Mazda<br /> 
<b>Value of Works Portion:</b> $90K-$120K range<br />
<b>Year:</b> 2009<br />
<b>Land Area:</b> 5,205m2<br />
</p>
<p> 
<b>Project Description</b><br /><br />
Australian Wetlands was engaged as both landscape and revegetation contractor for the brand new Mazda sales dealership at 81 Regentville Rd, Jamisontown (Western Sydney). 
</p>
<p>
<b>Services Provided</b><br /><br />
Australian Wetlands scope of work included:
<ul>
	<li>1,862m2 of carpark landscaping including soil, mulch, planting, stakes & ties, decomposed granite, timber edge, irrigation system, maintenance (3-months)</li>
	<li>1,289 m2 grass seeding to repair areas disturbed by the development</li>
	<li>2,054m2 of revegetation works including soil testing, soil amelioration, mulching, timber edge & planting of tubes, establishment watering & 24-months maintenance</li>
</ul>
The project demonstrates Australian Wetlands ability to provide both landscaping and revegetation service for our clients.   
</p>

<b>Project Description</b><br /><br />

Waterside Green is a 400-lot residential estate located in Western Sydney. Designed and developed by Stockland, the estate won the Urban Living Design Excellence Award from the Urban Design Institute of Australia. The estate boasts a wealth of open-space including five main lakes with restored bushland that can be viewed from 6km
</p>
<b>Services Provided</b><br /><br />

Australian Wetlands was engaged to revegetate the site in accordance with planting plans and DNR 3a permit. Our ecological services to the project included:<br />
<ul>
<li>Nursery - local provenance seed collection and propagation</li>
<li>Soil testing and soil amelioration</li>
<li>Large-scale revegetation and wetland landscaping</li> 
<li>Temporary irrigation</li>
<li>Weed management</li>
<li>2-year plant establishment maintenance period</li> 
<li>Extended long-term maintenance</li>
</ul>

The ongoing success of this site is a showcase of Australian Wetlandsâ€™ ability to undertake large-scale on-ground projects with a strong ecological component, while still providing high-quality and visually-pleasing landscaping that greatly enhances the amenity value of estate development.

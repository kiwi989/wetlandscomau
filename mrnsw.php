<p>
<b>Project Description</b><br /><br />
Kogarah's local government area (LGA) is located 14km south of the Sydney CBD. Moore Reserve is located within Kogarah LGA. Australian Wetlands was engaged by the local council to provide erosion control solutions to minimise scouring in two separate bushland areas.
</p>
<p>
<b>Services Provided</b><br /><br />
Australian Wetlands provided the following services:
<ul>
	<li>Engagement of designer to work with our construction team to provide ecological sensitive solutions</li> 
	<li>Shaping & minor earthworks</li> 
	<li>Sandstone rockwork to swales, weirs, settling ponds</li> 
	<li>Gabion basket wall installations on an extremely steep embankment</li> 
	<li>Shrub removal</li> 
	<li>Jute matting</li> 
	<li>Planting</li>
</ul>
The project demonstrates Australian Wetlandsâ€™ ability to achieve a successful erosion control outcomes with the use of machinery and labour within ecologically sensitive and access restricted environments.
